    //cuando escoges el almacen esto imprime los productos existentes en el y su cantidad
    private void Imprimir(int almacen) {
        contenido = "";
        System.out.println("llegue");
        if (!m.getGrafo().getAlmacenes()[almacen].getProductos().EsVacio()) {
            Producto aux = m.getGrafo().getAlmacenes()[almacen].getProductos().getpFirst();
            for (int j = 0; j < m.getGrafo().getAlmacenes()[almacen].getProductos().getSize(); j++) {
                System.out.print(aux.getNombre() + " ");

                contenido += aux.getNombre() + "    Cantidad: " + Integer.toString(aux.getCantidad()) + "\n";

                aux = aux.getPnext();

            }

        }
        jTextArea1.setText(contenido);
    }

