    private void aggNuevoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_aggNuevoActionPerformed
        // TODO add your handling code here:
        boolean found = false;
        try {

            nombreAux = this.inputProducto.getText();
            cantidad = Integer.parseInt(this.inputCant.getText());

            if (this.inputProducto.getText() == null && this.inputCant.getText() != null) {
                JOptionPane.showMessageDialog(this, "Por favor ingrese el nombre del producto.");
            }
            if (cantidad > 0) {
                if (!m.getGrafo().getAlmacenes()[m.getGrafo().getIndex(nombre)].getProductos().EsVacio()) {
                    Producto aux = m.getGrafo().getAlmacenes()[m.getGrafo().getIndex(nombre)].getProductos().getpFirst();
                    for (int j = 0; j < m.getGrafo().getAlmacenes()[m.getGrafo().getIndex(nombre)].getProductos().getSize(); j++) {
                        if (nombreAux.equalsIgnoreCase(aux.getNombre())) {
                            found = true;
                            break;
                        }

                        aux = aux.getPnext();

                    }

                }
                if (!found) {
                    System.out.println("Encontado");
                    m.getGrafo().buscarAlmacen(nombre).getProductos().InsertarFinal(nombreAux, cantidad);
                } else {
                    JOptionPane.showMessageDialog(this, "No se puede agregar este producto debido a que ya existe.");
                }

                inputProducto.setText("");
                inputCant.setText("");

                this.Imprimir(m.getGrafo().getIndex(nombre));
            } else {
                JOptionPane.showMessageDialog(this, "Ingrese un número positivo.");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Por favor ingrese una cantidad valida.");
        }
    }//GEN-LAST:event_aggNuevoActionPerformed

